package com.maveric.employeeDetails;

public class FinalBillingClass {
	
	private String employeeId;
	private String employeeName;
	private String subProjectId;
	private String subProjectName;
	private double totalOnsiteDays;
	private double totalOffshoreDays;
	private String errMessage;
	
	public String getEmployeeId() {
		return employeeId;
	}
	public void setEmployeeId(String employeeId) {
		this.employeeId = employeeId;
	}
	public String getEmployeeName() {
		return employeeName;
	}
	public void setEmployeeName(String employeeName) {
		this.employeeName = employeeName;
	}
	public String getSubProjectId() {
		return subProjectId;
	}
	public void setSubProjectId(String subProjectId) {
		this.subProjectId = subProjectId;
	}
	public String getSubProjectName() {
		return subProjectName;
	}
	public void setSubProjectName(String subProjectName) {
		this.subProjectName = subProjectName;
	}
	public double getTotalOnsiteDays() {
		return totalOnsiteDays;
	}
	public void setTotalOnsiteDays(double totalOnsiteDays) {
		this.totalOnsiteDays = totalOnsiteDays;
	}
	public double getTotalOffshoreDays() {
		return totalOffshoreDays;
	}
	public void setTotalOffshoreDays(double totalOffshoreDays) {
		this.totalOffshoreDays = totalOffshoreDays;
	}
	public String getErrMessage() {
		return errMessage;
	}
	public void setErrMessage(String errMessage) {
		this.errMessage = errMessage;
	}
	
	

}
